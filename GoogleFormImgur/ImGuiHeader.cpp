void ImGuiHeaderClass::RenderImguiMenu() {
	ImGui::PushItemWidth(-50);
	if (ImGui::Button("Start",ImVec2(200,20))) {
		SGlobal.ShouldRun = TRUE;
		if (!SGlobal.Initialized) {
			G_CCurlClass.StartThreads();
		}

	}
	ImGui::SameLine();
	if (ImGui::Button("Stop", ImVec2(200, 20))) {
		SGlobal.ShouldRun = FALSE;
	}
	ImGui::Checkbox("Debug", &(SGlobal.CheckBoxDebug));
	ImGui::PushItemWidth(-200);
	ImGui::Text("Responses Sent: ");
	ImGui::SameLine();
	ImGui::Text((std::to_string(SGlobal.TimesSent)).c_str());
	ImGui::Text("Threads: ");
	ImGui::SameLine();
	ImGui::Text((std::to_string(SGlobal.AmountThreads)).c_str());

	PROCESS_MEMORY_COUNTERS_EX pmc;
	GetProcessMemoryInfo(GetCurrentProcess(), (PROCESS_MEMORY_COUNTERS*)&pmc, sizeof(pmc));
	SIZE_T virtualMemUsedByMe = pmc.PrivateUsage;
	ImGui::Text(std::to_string(virtualMemUsedByMe * pow(10, -6)).c_str());

}